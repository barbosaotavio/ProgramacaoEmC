#include <stdio.h>

int main(){

    /*
    Operadores Relacionais

    > (maior que)
    < (menor que)
    >= (maior ou igual a)
    <= (menor ou igual a)
    == (igual a)
    != (diferente de)

    1 para verdadeiro
    0 para falso
    */

    int a = 10;
    int b = 20;

    printf("a > b: %d \n", a > b);
    printf("a < b: %d \n", a < b);
    printf("a == b: %d \n", a == b);
    printf("a != b: %d \n", a != b);
    

}